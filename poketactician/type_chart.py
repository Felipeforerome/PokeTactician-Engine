TYPE_CHART: Final[NDArray[np.float32]] = np.asarray(_TYPE_CHART_DATA, dtype=np.float32)
TYPE_CHART.setflags(write=False)


def get_type_index(type_name: str) -> int:
    """Return the index for a lowercase Pokemon type name."""
    try:
        return TYPE_INDEX[type_name]
    except KeyError as exc:
        valid_names = ", ".join(TYPE_NAMES)
        raise ValueError(f"Unknown Pokemon type {type_name!r}. Expected one of: {valid_names}") from exc


def get_type_chart() -> NDArray[np.float32]:
    """Return the immutable attack-vs-defense effectiveness chart."""
    return TYPE_CHART


def get_matchup_multiplier(attacking_type: str, defending_types: str | tuple[str, ...]) -> float:
    """Return the combined multiplier for one attack type against one or more defending types."""
    if isinstance(defending_types, str):
        defending_type_names = (defending_types,)
    else:
        defending_type_names = defending_types

    attack_index = get_type_index(attacking_type)
    multiplier = 1.0
    for defending_type in defending_type_names:
        defense_index = get_type_index(defending_type)
        multiplier *= float(TYPE_CHART[attack_index, defense_index])
    return multiplier


if TYPE_CHART.shape != (NUMBER_OF_TYPES, NUMBER_OF_TYPES):
    raise ValueError(f"Pokemon type chart must be {NUMBER_OF_TYPES}x{NUMBER_OF_TYPES}; got {TYPE_CHART.shape}")
